#include "else.h"

double		ft_pow(double x, double y)
{
	double a;

	a = x;
	if ((int)y == 0)
		return (1);
	if ((int)x == 1 || (int)x == 0 || (int)y == 1)
		return (x);
	while (y > 1)
	{
		x = x * a;
		y--;
	}
	return (x);
}
